/**
 * Default minimum grid width for code blocks.
 * This is the minimum width (in grid units) that a code block can have.
 */
export const CODE_BLOCK_MIN_GRID_WIDTH = 32;

/**
 * Minimum grid width for code blocks with piano keyboards.
 * Piano keyboards require more horizontal space than regular code blocks.
 */
export const PIANO_KEYBOARD_MIN_GRID_WIDTH = 48;
